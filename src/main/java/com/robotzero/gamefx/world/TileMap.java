    public TileMap() {
        this.generateTilePositions();
    }

    private void generateTilePositions() {
        for(int row = 0;
            row < 9;
            ++row)
        {
            for(int column = 0;
                column < 17;
                ++column) {
                int tileID = tiles00[row][column];
                float color = 0.5f;
                if (tileID == 1) {
                    color = 1.0f;
                }
                tilePositions.put(new Vector3f(column * tileWidth, row * tileHeight, 0), color);
            }
        }
    }

